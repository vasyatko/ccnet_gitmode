namespace ThoughtWorks.CruiseControl.WebDashboard.Dashboard
{
    /// <summary>
    /// Defines the location of the success indicator bar.
    /// </summary>
    public enum IndicatorBarLocation
    {
        /// <summary>
        /// The bar will not appear at all.
        /// </summary>
        None,

        /// <summary>
        /// The bar will appear at the top.
        /// </summary>
        Top,

        /// <summary>
        /// The bar will appear at the bottom.
        /// </summary>
        Bottom,

        /// <summary>
        /// The bar will appear both top and bottom.
        /// </summary>
        TopAndBottom,
    }
}
