namespace ThoughtWorks.CruiseControl.Core
{
    /// <summary>
    /// Defines the display level of a property.
    /// </summary>
    public enum DisplayLevel
    {
        /// <summary>
        /// The field is not displayed.
        /// </summary>
        None,
        /// <summary>
        /// The field is optional.
        /// </summary>
        Optional,
        /// <summary>
        /// The field is required.
        /// </summary>
        Required,
    }
}
